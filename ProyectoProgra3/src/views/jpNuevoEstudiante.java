    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        if (!(("".equals(txtNombreEstudiante.getText())) || ("".equals(txtCarnetEstudiante.getText())) || ("Seleccione...".equals(cmbAnniosCarrera.getSelectedItem())))) {
            Estudiantes nuevoEstudiante = new Estudiantes();
            nuevoEstudiante.setNombresApellidos(txtNombreEstudiante.getText());
            nuevoEstudiante.setCarnet(txtCarnetEstudiante.getText());
            String annio = cmbAnniosCarrera.getSelectedItem().toString();
            int anio = 0;
            switch (annio){
                case "Primer año." -> anio = 1;
                case "Segundo año." -> anio = 2;
                case "Tercer año." -> anio = 3;
                case "Cuarto año." -> anio = 4;
                case "Quinto año." -> anio = 5;
            }
            nuevoEstudiante.setAnnioCarrera(anio);
            EstudianteControlador controlador = new EstudianteControlador();
            controlador.insertarEstudiante(nuevoEstudiante);
            JOptionPane.showMessageDialog(null, "EL REGISTRO FUE INSERTADO EXITOSAMENTE.","REGISTRO INSERTADO.", JOptionPane.INFORMATION_MESSAGE);
            limpiarTodo();
            cargarEstudiantes();
        }
        else{
            JOptionPane.showMessageDialog(null, "ASEGURESE DE HABER LLENADO COMPLETMENTE EL FORMULARIO.","ERROR.", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

