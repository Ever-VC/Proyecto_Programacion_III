    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        if (!(("".equals(txtNombreEstudiante.getText())) || ("".equals(txtCarnetEstudiante.getText())) || ("Seleccione...".equals(cmbAnniosCarrera.getSelectedItem())))) {
            int verificar = JOptionPane.showConfirmDialog(null, "¿ESTA SEGURO QUE DESEA ELIMINAR EL REGISTRO?", "CONFIRME.", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (verificar == JOptionPane.YES_OPTION){
                int idElim = Integer.parseInt(txtIdEstudiante.getText());
                //Busca los talleres a los que asiste el estudiante para eliminar esos registros
                buscarCursoAsistido(idElim);
                EstudianteControlador controlador = new EstudianteControlador();
                controlador.eliminarEstudiante(idElim);
                JOptionPane.showMessageDialog(null, "EL REGISTRO FUE ELIMINADO EXITOSAMENTE.","REGISTRO ELIMINADO.", JOptionPane.INFORMATION_MESSAGE);
                limpiarTodo();
                cargarEstudiantes();
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "ASEGURESE DE HABER SELECCIONADO EL RESGISTRO QUE DESEA ELIMINAR.","ERROR.", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void cargarEstudiantes(){
        DefaultTableModel modelo = new DefaultTableModel();
        modelo = (DefaultTableModel)tblEstudiantes.getModel();
        modelo.setRowCount(0);//Limpia todas los registros de la tabla (Basicamente le indica que no quiere ninguna fila)
        EstudianteControlador controlador = new EstudianteControlador();
        List<Estudiantes> lstEstudiantes = controlador.getLisEstudiantes();
        //jpNuevoEstudiante.
        for (Estudiantes item : lstEstudiantes) {
            int annio = item.getAnnioCarrera();
            String anio = "";
            switch (annio){
                case 1 -> anio = "Primer año.";
                case 2 -> anio = "Segundo año.";
                case 3 -> anio = "Tercer año.";
                case 4 -> anio = "Cuarto año.";
                case 5 -> anio = "Quinto año.";
            }
            modelo.addRow(new Object[]{item.getIdEstudiante(), item.getNombresApellidos(), item.getCarnet(), anio});
        }
    }
    
    private void buscarCursoAsistido(int idEstudiante){
        AsistenciaControlador controlador1 = new AsistenciaControlador();
        List<Asistencia> lstAsistencia = controlador1.getLisTalleresPonencias();
        List<Integer> idAsistencia = new ArrayList<Integer>();
        int i = 0;
        for (Asistencia item : lstAsistencia) {
            if (idEstudiante == item.getIdEstudiante().getIdEstudiante()) {
                idAsistencia.add(item.getIdAsistencia());
            }
            i ++;
        }
        
        for (int j : idAsistencia) {
            AsistenciaControlador controlador = new AsistenciaControlador();
            controlador.eliminarAsistencia(j);
        }
        
    }
    
